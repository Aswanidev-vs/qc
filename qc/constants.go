package qc

import "math"

// Mathematical constants
const (
	// Pi is the ratio of a circle's circumference to its diameter.
	Pi = math.Pi

	// E is Euler's number, the base of natural logarithms.
	E = math.E

	// Phi is the golden ratio (1 + sqrt(5)) / 2.
	Phi = math.Phi

	// Sqrt2 is the square root of 2.
	Sqrt2 = math.Sqrt2

	// SqrtPi is the square root of Pi.
	SqrtPi = math.SqrtPi

	// SqrtE is the square root of E.
	SqrtE = math.SqrtE

	// Ln2 is the natural logarithm of 2.
	Ln2 = math.Ln2

	// Ln10 is the natural logarithm of 10.
	Ln10 = math.Ln10

	// Log2E is the base-2 logarithm of E.
	Log2E = math.Log2E

	// Log10E is the base-10 logarithm of E.
	Log10E = math.Log10E

	// DegPerRad is the number of degrees per radian (180 / Pi).
	DegPerRad = 180.0 / Pi

	// RadPerDeg is the number of radians per degree (Pi / 180).
	RadPerDeg = Pi / 180.0
)
